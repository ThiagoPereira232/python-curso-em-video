a = input('\033[31mDigite algo: \033[m')
print('O tipo primitivo desse valor é \033[33m', type(a), '\033[m')
print('Só tem espaços? \033[33m\033[33m', a.isspace(), '\033[m')
print('É um número? \033[33m', a.isnumeric(), '\033[m')
print('É alfabetico? \033[33m', a.isalpha(), '\033[m')
print('É alfanúmerico? \033[33m', a.isalnum(), '\033[m')
print('Está em maiúsculas?\033[33m', a.isupper(), '\033[m')
print('Está em minúsculas?\033[33m', a.islower(), '\033[m')
print('Está capitalizada?\033[33m', a.istitle(), '\033[m')
